/**
 * ビルド時の config / manifest 注入ロジック（純粋関数）。
 *
 * @typedef {object} BuildOptions
 * @property {string} builtAt - ISO8601 datetime, empty string for dev
 *
 * @typedef {import('../src/shared/pattern-matcher').Pattern} Pattern
 * @typedef {object} BundledTemplate
 * @property {string} type
 * @property {string} body
 * @typedef {object} Occupation
 * @property {string} id
 * @property {string} displayName
 * @property {string[]} matchQualifications
 * @property {Pattern[]} patterns
 * @property {BundledTemplate[]} templates
 * @typedef {object} Company
 * @property {string} companyId
 * @property {string} displayName
 * @property {'welme'|'comedical'} platform
 * @property {Occupation[]} occupations
 * @typedef {object} ScoutConfig
 * @property {Company[]} companies
 */

export function renderBuildConfig(opts) {
  const escape = (s) => String(s).replace(/'/g, "\\'");
  return `// AUTO-GENERATED by scripts/build-config-inject. Do not edit.
export interface BuildConfig {
  builtAt: string;
}

export const BUILD_CONFIG: BuildConfig = {
  builtAt: '${escape(opts.builtAt)}',
};
`;
}

export function renderBundledScoutConfig(config) {
  return `// AUTO-GENERATED by scripts/build-config-inject. Do not edit.
import type { ScoutConfig } from './types';

export const BUNDLED_SCOUT_CONFIG: ScoutConfig = ${JSON.stringify(config, null, 2)};
`;
}

/** manifest.json をそのまま返す（現時点では何も差し替えない） */
export function patchManifest(manifest) {
  return { ...manifest };
}

/** 配布zip名 */
export function zipName(opts) {
  const date = opts.builtAt ? opts.builtAt.slice(0, 10).replace(/-/g, '') : 'dev';
  return `scout-extension-${date}.zip`;
}
