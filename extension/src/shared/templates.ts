/** template_typeからテンプレートを検索し、personalized_textを挿入してfull_scout_textを生成 */
export function buildFullScoutText(
  company: string,
  templateType: string,
  personalizedText: string
): string | null {
  const templates = COMPANY_TEMPLATES[company];
  if (!templates) return null;

  // template_typeの表記揺れを吸収（「パート初回」→「パート_初回」等）
  const normalized = templateType.replace(/[_\s・]/g, '_');
  const template = templates.find((t) => {
    const tNorm = t.type.replace(/[_\s・]/g, '_');
    return tNorm === normalized;
  });
  if (!template) return null;

  return template.body.replace('{personalized_text}', personalizedText);
}
