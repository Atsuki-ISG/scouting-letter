/**
 * ビルド時の config / manifest 注入ロジック（純粋関数）。
 * build.js と vitest の双方から import されるため .js にしている。
 *
 * @typedef {'jobmedley' | 'welme'} MediumId
 * @typedef {object} BuildOptions
 * @property {string} companyId
 * @property {MediumId} medium
 * @property {string} displayName
 * @property {string} builtAt - ISO8601 datetime, empty string for dev
 */

/** @type {Record<MediumId, string[]>} */
const HOSTS_BY_MEDIUM = {
  jobmedley: ['https://job-medley.com/*', 'https://*.job-medley.com/*'],
  welme: ['https://welme.jp/*', 'https://*.welme.jp/*'],
};

/**
 * src/shared/build-config.ts の内容を生成する。
 * @param {BuildOptions} opts
 * @returns {string}
 */
export function renderBuildConfig(opts) {
  const escape = (s) => s.replace(/'/g, "\\'");
  return `// AUTO-GENERATED by scripts/build-config-inject. Do not edit.
import type { MediumId } from './medium-adapter';

export interface BuildConfig {
  companyId: string;
  medium: MediumId;
  displayName: string;
  builtAt: string;
}

export const BUILD_CONFIG: BuildConfig = {
  companyId: '${escape(opts.companyId)}',
  medium: '${escape(opts.medium)}' as MediumId,
  displayName: '${escape(opts.displayName)}',
  builtAt: '${escape(opts.builtAt)}',
};
`;
}

/**
 * manifest.json を媒体・会社情報で上書きする。
 * - 対象媒体以外の host_permissions / matches は取り除く
 * - name は displayName で上書き
 * - 元 manifest の無関係なフィールド（script.google.com等）は保持
 *
 * @template T
 * @param {T} manifest
 * @param {BuildOptions} opts
 * @returns {T}
 */
export function patchManifest(manifest, opts) {
  const mediumHosts = HOSTS_BY_MEDIUM[opts.medium];
  const mediumHostRe = /job-medley\.com|welme\.jp/;
  const keepHost = (h) => {
    if (!mediumHostRe.test(h)) return true;
    return mediumHosts.some((mh) => mh === h);
  };

  const patched = {
    ...manifest,
    name: opts.displayName,
    host_permissions: [
      ...manifest.host_permissions.filter(keepHost),
      ...mediumHosts.filter((mh) => !manifest.host_permissions.includes(mh)),
    ],
    content_scripts: manifest.content_scripts.map((cs) => ({
      ...cs,
      matches: mediumHosts,
    })),
  };
  return patched;
}

/**
 * 配布zipのファイル名を生成する。
 * @param {BuildOptions} opts
 * @returns {string}
 */
export function scoutExtensionZipName(opts) {
  const date = opts.builtAt
    ? opts.builtAt.slice(0, 10).replace(/-/g, '')
    : 'dev';
  return `${opts.companyId}-${opts.medium}-extension-${date}.zip`;
}
