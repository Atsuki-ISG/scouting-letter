/**
 * ビルド時の config / manifest 注入ロジック（純粋関数）。
 * build.js と vitest の双方から import されるため .js。
 *
 * @typedef {object} BuildOptions
 * @property {string} companyId
 * @property {string} displayName
 * @property {string} builtAt - ISO8601 datetime, empty string for dev
 *
 * @typedef {import('../src/shared/pattern-matcher').Pattern} Pattern
 * @typedef {object} BundledTemplate
 * @property {string} type
 * @property {string} body
 * @typedef {object} BundledCompanyConfig
 * @property {string} companyId
 * @property {string} displayName
 * @property {Pattern[]} patterns
 * @property {BundledTemplate[]} templates
 */

/**
 * src/shared/build-config.ts のソースを生成する。
 * @param {BuildOptions} opts
 * @returns {string}
 */
export function renderBuildConfig(opts) {
  const escape = (s) => s.replace(/'/g, "\\'");
  return `// AUTO-GENERATED by scripts/build-config-inject. Do not edit.
export interface BuildConfig {
  companyId: string;
  displayName: string;
  builtAt: string;
}

export const BUILD_CONFIG: BuildConfig = {
  companyId: '${escape(opts.companyId)}',
  displayName: '${escape(opts.displayName)}',
  builtAt: '${escape(opts.builtAt)}',
};
`;
}

/**
 * companies/[会社]/ から抽出した config を拡張バンドル用の TS に
 * シリアライズする。
 * @param {BundledCompanyConfig} cfg
 * @returns {string}
 */
export function renderBundledCompanyConfig(cfg) {
  return `// AUTO-GENERATED by scripts/build-config-inject. Do not edit.
import type { Pattern } from './pattern-matcher';

export interface BundledTemplate {
  type: string;
  body: string;
}

export interface BundledCompanyConfig {
  companyId: string;
  displayName: string;
  patterns: Pattern[];
  templates: BundledTemplate[];
}

export const BUNDLED_COMPANY_CONFIG: BundledCompanyConfig = ${JSON.stringify(cfg, null, 2)};
`;
}

/**
 * manifest.json の name を displayName で上書き。
 * 全フィールド保持、name のみ差し替え。
 * @template T
 * @param {T} manifest
 * @param {BuildOptions} opts
 * @returns {T}
 */
export function patchManifest(manifest, opts) {
  return { ...manifest, name: opts.displayName };
}

/**
 * 配布zipのファイル名。
 * @param {BuildOptions} opts
 * @returns {string}
 */
export function zipName(opts) {
  const date = opts.builtAt ? opts.builtAt.slice(0, 10).replace(/-/g, '') : 'dev';
  return `${opts.companyId}-comedical-extension-${date}.zip`;
}
